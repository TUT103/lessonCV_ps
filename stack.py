import numpy as np

a = np.array([1, 2, 3])
b = np.array([2, 3, 4])
print(np.stack((a, b), axis=0))
print(np.stack((a, b), axis=1))
print("=============================================")

c = np.array([3, 4, 5])
print(np.stack((a, b, c), axis=0))
print(np.stack((a, b, c), axis=1))
print("=============================================")

